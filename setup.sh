# Social Network API Setup Instructions

## Requirements

- Docker
- Docker Compose

## Setup

1. **Download and Extract the Package**

   Download the provided package and extract it. You should have the following files:

   - `docker-compose.yml`
   - `setup.sh`
   - `social_network_api_web_image.tar`
   - `social_network_api_db_image.tar`

2. **Run the Setup Script**

   Open a terminal in the directory where the files are located and run:

   ```bash
   ./setup.sh
